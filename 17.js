  // Функція обробки атаки гравця
  function playerAttack() {
    // Перебираємо копію масиву ворогів, щоб безпечно видаляти з оригіналу
    for (let i = 0; i < enemies.length; i++) {
      const enemy = enemies[i];
      // Перевірка чи ворог в зоні досяжності (колізія)
      if (isColliding(player, enemy)) {
        // Наносимо шкоду ворогу
        enemy.health -= 50;  // припустимо, удар знімає 50 здоров'я
        // Трохи "відштовхнемо" ворога від гравця після удару
        if (player.facing === 'right') {
          enemy.x += 20;
        } else {
          enemy.x -= 20;
        }
        // Якщо ворога вбито
        if (enemy.health <= 0) {
          score += 10;                 // +10 очок за переможеного ворога
          enemies.splice(i, 1);        // видаляємо ворога з масиву
          i--; // коригуємо індекс через видалення
        }
      }
    }
    // Перевірка: чи пройдено рівень (немає ворогів)
    if (enemies.length === 0) {
      if (level < 2) {
        // Завершили рівень 1, переходимо на рівень 2
        loadLevel(level + 1);
      } else {
        // Вороги 2-го рівня теж подолані - гра виграна
        gameState = 'win';
        bgMusic.pause();
      }
    }
  }
